def _prov_key(provider: str) -> str:
    return (provider or "").strip().lower()


def scale_syllabus(
    template: list[tuple[str, int, list[str]]],
    total_h: int,
) -> list[tuple[str, int, list[str]]]:
    base = sum(h for _, h, _ in template) or total_h
    ratio = total_h / base
    out: list[tuple[str, int, list[str]]] = []
    for name, h, topics in template:
        nh = max(8, int(h * ratio))
        out.append((name, nh, topics))
    # подогнать сумму
    diff = total_h - sum(h for _, h, _ in out)
    if out and diff:
        n0, h0, t0 = out[0]
        out[0] = (n0, max(8, h0 + diff), t0)
    return out


def syllabus_for_provider(course: dict, total_h: int) -> list[tuple[str, int, list[str]]] | None:
    pk = _prov_key(course.get("provider", ""))
    cat = course.get("cat", "courses")

    if pk in PROVIDER_SYLLABI:
        return scale_syllabus(PROVIDER_SYLLABI[pk], total_h)

    if cat == "interactive":
        return scale_syllabus(INTERACTIVE_DEFAULT, min(total_h, 80))

    if cat == "courses" or pk:
        return scale_syllabus(MOOC_DEFAULT, total_h)

    return None
