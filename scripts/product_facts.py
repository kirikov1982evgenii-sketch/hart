def _lang_slug(course_id: str) -> str:
    if course_id.startswith("hart-lang-"):
        return course_id.replace("hart-lang-", "")
    return ""


def clean_about(desc: str, title: str, provider: str) -> str:
    """Только факты, без маркетинга HART."""
    lines = []
    if desc:
        lines.append(desc.strip())
    if provider and provider != "Клуб знаний HART":
        lines.append(f"Источник материалов: {provider}.")
    return "\n".join(lines)


def product_sheet(course: dict) -> list[tuple[str, str]]:
    """Пары «поле — значение» для справки."""
    cid = course.get("id", "")
    rows: list[tuple[str, str]] = [
        ("Продукт", course.get("title", "")),
        ("Провайдер", course.get("provider", "")),
    ]
    if course.get("level") and course.get("level") != "—":
        rows.append(("Уровень", course["level"]))
    if course.get("duration"):
        rows.append(("Объём программы", course["duration"]))
    if course.get("desc"):
        rows.append(("Кратко", course["desc"]))
    tags = course.get("tags") or []
    if tags:
        rows.append(("Теги", ", ".join(tags)))
    url = course.get("partnerUrl") or course.get("url") or ""
    if url:
        rows.append(("Ссылка", url))
    if course.get("free") is True:
        rows.append(("Доступ", "Бесплатный / базовый тариф"))
    elif course.get("free") is False:
        rows.append(("Доступ", "Платный"))

    kb = BY_ID.get(cid, {})
    if kb:
        rows.append(("Что это", kb.get("what", "")))
        for i, f in enumerate(kb.get("features") or [], 1):
            rows.append((f"Возможность {i}", f))
        for i, h in enumerate(kb.get("how") or [], 1):
            rows.append((f"Шаг использования {i}", h))
        if kb.get("for_whom"):
            rows.append(("Для кого", kb["for_whom"]))
        if kb.get("limits"):
            rows.append(("Ограничения", kb["limits"]))

    prov = course.get("provider", "")
    if prov in PROVIDER_SHEET and not kb:
        for i, line in enumerate(PROVIDER_SHEET[prov], 1):
            rows.append((f"О платформе {i}", line))

    for i, item in enumerate(course.get("learn") or [], 1):
        rows.append((f"Результат {i}", item))

    return [(k, v) for k, v in rows if v]


def module_points(course: dict, module: str, idx: int) -> list[str]:
    """Конкретные пункты урока по модулю."""
    cid = course.get("id", "")
    kb = BY_ID.get(cid)
    if kb:
        feats = list(kb.get("features") or [])
        how = list(kb.get("how") or [])
        chunk = 3
        if idx == 0:
            out = [kb.get("what", "")]
            out.extend(feats[:chunk])
            return [x for x in out if x]
        if idx == 1:
            return feats[chunk : chunk + 4] or feats
        if idx == 2:
            return how[:4] or feats[-3:]
        if idx == 3:
            out = how[4:] if len(how) > 4 else []
            if kb.get("limits"):
                out.append(f"Ограничения: {kb['limits']}")
            if kb.get("for_whom"):
                out.append(f"Аудитория: {kb['for_whom']}")
            return out or [f"Закрепление: {module}"]

    if cid == "hart-poker-mastery" and idx in POKER_MODULES:
        return POKER_MODULES[idx]

    slug = _lang_slug(cid)
    if slug == "python" and idx < len(LANG_MODULE_POINTS["python"]):
        return LANG_MODULE_POINTS["python"][idx]

    if slug:
        name = course.get("title", "").replace("Изучение ", "").split(" —")[0]
        return [
            f"Тема модуля: {module}",
            f"Язык: {name}",
            f"Официальные материалы: {course.get('url', '')}",
            "Практика: выполнить упражнения из документации к этому разделу",
            "Проверка: написать 10–20 строк кода по теме модуля",
        ]

    desc = course.get("desc", "")
    learn = course.get("learn") or []
    url = course.get("partnerUrl") or course.get("url", "")
    points = []
    if idx < len(learn):
        points.append(learn[idx])
    if desc and idx == 0:
        points.append(desc)
    points.append(module)
    if url:
        host = urlparse(url).netloc or url
        points.append(f"Материалы: {url} ({host})")
    prov = course.get("provider", "")
    if prov and prov in PROVIDER_SHEET and idx < len(PROVIDER_SHEET[prov]):
        points.append(PROVIDER_SHEET[prov][idx])
    return points


def quiz_from_sheet(rows: list[tuple[str, str]], cid: str) -> list[dict]:
    """Тест только по фактам из справки."""
    import hashlib

    questions = []
    usable = [(k, v) for k, v in rows if len(v) > 12 and k not in ("Ссылка",)]
    for i, (label, value) in enumerate(usable[:6]):
        wrong_pool = [v for _, v in usable if v != value]
        wrong = wrong_pool[:3] if len(wrong_pool) >= 3 else [
            "Информация не указана в программе",
            "Только для платного тарифа без ограничений",
            "Не требует регистрации и интернета",
        ]
        opts = [value] + wrong[:3]
        tagged = list(enumerate(opts))
        tagged.sort(key=lambda x: hashlib.md5(f"{cid}-{i}-{x[0]}".encode()).hexdigest())
        shuffled = [t[1] for t in tagged]
        correct = shuffled.index(value)
        qtext = f"{label}: выберите верное утверждение"
        if len(label) < 30:
            qtext = f"По продукту — {label}?"
        questions.append(
            {
                "q": qtext,
                "options": shuffled,
                "correct": correct,
                "explain": f"{label}: {value}",
            }
        )
    return questions[:5]
